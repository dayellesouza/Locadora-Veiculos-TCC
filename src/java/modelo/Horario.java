package modelo;

import java.sql.Time;

/**
 *
 * @author Dayelle
 */
public class Horario {
    
    private int id;
    private Time hora;

    public Horario() {
    }

    public Horario( Time hora) {
        this.hora = hora;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Time getHora() {
        return hora;
    }

    public void setHora(Time hora) {
        this.hora = hora;
    }
}
